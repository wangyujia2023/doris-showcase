const normalize = (v) => (v === 'en' ? 'en' : 'zh')

export const locale = ref(normalize(localStorage.getItem(STORAGE_KEY)))

export function setLocale(lang) {
  locale.value = normalize(lang)
  localStorage.setItem(STORAGE_KEY, locale.value)
}

export const currentMessages = computed(() => messages[locale.value])

export function t(path) {
  const parts = path.split('.')
  let cur = messages[locale.value]
  for (const p of parts) {
    cur = cur?.[p]
    if (cur == null) return path
  }
  return cur
}

export const locales = [
  { value: 'zh', label: () => t('locale.zh') },
  { value: 'en', label: () => t('locale.en') },
]
